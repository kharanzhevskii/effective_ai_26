JMP start

start:
    ; =========================================================
    ; ШАГ 1: ВЕКТОРНАЯ КОНВЕРТАЦИЯ ПИКСЕЛЕЙ (uint8 -> fp16)
    ; =========================================================
    VSET VL, 784

    ; 1. Заливаем alt-буфер (Стр 215) значением -1.0_h (Черный)
    VSET VC, 0xD700
    VMOV.H VC, -1.0_h
    VWAIT

    ; 2. Заливаем рабочий буфер (Стр 204) значением +1.0_h (Белый)
    VSET VC, 0xCC00
    VMOV.H VC, 1.0_h
    VWAIT

    ; 3. Накладываем маску с планшета (0x00 или 0xFF)
    VSET VM, 0xC800       ; VM указывает на планшет (Стр 200)
    VSET VA, 0xD700       ; VA (alt) = буфер с -1.0
    VSET VC, 0xCC00       ; VC (dst) = буфер с +1.0
    
    VSEL.H VC, VA         ; Если планшет 0xFF -> оставляем +1.0 в VC
                          ; Если планшет 0x00 -> берем -1.0 из VA
    VWAIT                 ; Ждем окончания конвертации

    ; =========================================================
    ; ШАГ 2: СЛОЙ 1 (hidden = ReLU(W1 * X + b1))
    ; =========================================================
    ; VL = 784 остался с прошлого шага
    VSET VA, 0x0100       ; Матрица W1 (Стр 1, смещ 0)
    VSET VC, 0xD300       ; Буфер hidden_1 (Стр 211)
    MOV C, 32
.l1_dot:
    VSET VB, 0xCC00       ; Вектор X (наш готовый fp16 буфер)
    VDOT.H VC, VA, VB
    DEC C
    JNZ .l1_dot

    ; Bias
    VSET VL, 32
    VSET VA, 0xD300       ; hidden_1
    VSET VB, 0xC500       ; Вектор b1 (Стр 197)
    VADD.H VA, VA, VB
    VWAIT

    ; Активация ReLU
    VSET VA, 0xD300
    VMAX.H VA, VA, 0.0_h  ; ReLU: берем максимум между hidden_1 и 0.0
    VWAIT

    ; =========================================================
    ; ШАГ 3: СЛОЙ 2 (logits = W2 * hidden + b2)
    ; =========================================================
    ; VL = 32 остался с прошлого шага
    VSET VA, 0xC540       ; Матрица W2 (Стр 197, смещ 64)
    VSET VC, 0xD400       ; Буфер logits (Стр 212)
    MOV C, 10
.l2_dot:
    VSET VB, 0xD300       ; Вектор hidden_1
    VDOT.H VC, VA, VB
    DEC C
    JNZ .l2_dot

    ; Bias
    VSET VL, 10
    VSET VA, 0xD400       ; logits
    VSET VB, 0xC7C0       ; Вектор b2 (Стр 199, смещ 192)
    VADD.H VA, VA, VB
    VWAIT                 ; ВАЖНО: ждем завершения слоя перед поиском ответа

    ; =========================================================
    ; ШАГ 4: ПОИСК ОТВЕТА (Argmax) И ВЫВОД В КОНСОЛЬ
    ; =========================================================
    MOV DP, 212           ; Страница логитов
    MOV A, 0              ; Смещение
    MOV B, 0              ; Текущий индекс (0-9)
    MOV C, 0              ; Индекс победителя
    FMOV.H FHB, -100.0_h  ; Стартовый максимум

.argmax:
    FMOV.H FHA, [A]       ; Читаем логит
    FCMP.H FHA, FHB
    JC .skip              ; Если логит < максимума, пропускаем
    
    FMOV.H FHB, FHA       ; Нашли новый максимум
    MOV C, B              ; Запомнили его индекс
.skip:
    INC B
    ADD A, 2
    CMP B, 10
    JNZ .argmax

    ; Вывод цифры в консоль (0-9)
    MOV DP, 0
    MOV A, C
    ADD A, '0'            ; Превращаем индекс в ASCII
    MOV [232], A          ; Выводим на экран (Победа!)

    HLT

